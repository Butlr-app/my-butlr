export const Card = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn('bg-card text-card-foreground border border-border rounded-lg', className)}
      {...props}
    />
  )
)
Card.displayName = 'Card'

